// Контроль структуры
if (data.length !== 10) throw new Error(`Ожидалось 10 категорий, получено ${data.length}`);
for (const c of data) {
  if (c.qa.length !== 10) throw new Error(`Категория «${c.name}»: ${c.qa.length} вопросов вместо 10`);
}

const categories = [];
const questions = [];
data.forEach((c, ci) => {
  const categoryId = randomUUID();
  categories.push({ id: categoryId, name: c.name, position: ci + 1 });
  c.qa.forEach(([prompt, answer], qi) => {
    questions.push({
      id: randomUUID(),
      categoryId,
      type: 'text',
      prompt,
      answer,
      media: null,
      position: qi + 1,
    });
  });
});

const bank = { categories, questions };
const zip = new AdmZip();
zip.addFile('bank.json', Buffer.from(JSON.stringify(bank, null, 2), 'utf-8'));
writeFileSync('packs/БАНК-10x10.zip', zip.toBuffer());

console.log(`packs/БАНК-10x10.zip собран: ${categories.length} категорий, ${questions.length} вопросов (тип text)`);
